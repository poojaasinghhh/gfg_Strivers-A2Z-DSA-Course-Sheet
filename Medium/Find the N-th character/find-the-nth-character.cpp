class Solution{
  public:
    char nthCharacter(string s, int r, int n) {
        //code here
           int cnt = 0;
        while(r!=0){
            cnt += n%2;
            n /= 2;
            r--;
        }
        if(cnt %2==0) return s[n];
        else return s[n] == '0' ? '1' : '0';
    }
};

