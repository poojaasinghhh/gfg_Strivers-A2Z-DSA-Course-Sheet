class Solution {
  public:
    vector<int> removeDuplicate(vector<int>& arr) {
        // code here
        
        set<int>temp;
        
        vector<int>ans;
        
        
        for(auto i:arr){
            if(temp.find(i)==temp.end()){
                temp.insert(i);
                ans.push_back(i);
            }
        }
        
        return ans;
    }
};


